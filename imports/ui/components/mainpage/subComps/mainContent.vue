<template>
    <v-sheet>
      <v-alert
        outlined
        type="success"
        text
        v-model="alert"
        border="left"
        close-text="Close Alert"
        dismissible
      >
      Sie haben das Modul {{currentModule.name}} abonniert!
      </v-alert>
      <topBar :user="user" v-on:drawNav="drawNav()"></topBar>
      <tabs :currentModule="currentModule"></tabs>
      <v-container class="fill-height">
        <v-row align="center" justify="center">
          <v-carousel
            :class="[{ mobileSlider: $vuetify.breakpoint.mobile }]"
            cycle
            show-arrows-on-hover
          >
            <v-carousel-item
              v-for="(item, i) in items"
              :key="i"
              :src="item.src"
            ></v-carousel-item>
          </v-carousel>
        </v-row>
      </v-container>
      <v-navigation-drawer :class="[{'mainNav' : !$vuetify.breakpoint.mobile}, {'mainNavMobile': $vuetify.breakpoint.mobile}]" temporary v-model="drawer">
        <v-list-item>
          <v-list-item-content>
            <v-img
              src="https://upload.wikimedia.org/wikipedia/commons/e/e0/Technische_Hochschule_Mittelhessen_Logo.svg"
            ></v-img>
          </v-list-item-content>
        </v-list-item>
        <v-divider></v-divider>
        <toDoBase></toDoBase>
        <v-row align="center">
          <v-col cols="12" sm="6">
            <v-subheader :class="[{'display-3' : $vuetify.breakpoint.mobile}, {'display-1': !$vuetify.breakpoint.mobile}]">Filter options:</v-subheader>
          </v-col>
          <v-col cols="12" sm="6">
            <v-select
              :class="[{'display-5' : $vuetify.breakpoint.sm}, {'display-5': !$vuetify.breakpoint.mobile}]"
              v-model="e6"
              :items="filterOptions"
              :menu-props="{ maxHeight: '400' }"
              label="Select"
              @input="filterModules()"
              persistent-hint
            >
            </v-select>
          </v-col>
        </v-row>
        <v-divider></v-divider>
        <v-list dense>
          <v-list-item v-for="item in modules" :key="item.name" link>
            <v-btn icon class="mr-2">
              <v-icon
                color="primary"
                @click="
                  subscribeModule(item);
                  alert = true;"
                >mdi-plus</v-icon
              >
            </v-btn>
            <v-list-item-content>
              <v-list-item-title @click="openModule(item)">{{
                item.name
              }}</v-list-item-title>
            </v-list-item-content>
          </v-list-item>
        </v-list>
      </v-navigation-drawer>
    </v-sheet>
</template>

<script>
import topBar from "../../smallComps/topBar";
import tabs from "../subComps/tabs";
import toDoBase from "../ToDos/toDoBase.vue";
import Modules from "../../../../api/collections/Modules";
export default {
  name: "mainContent",
  components: {
    topBar,
    tabs,
    toDoBase,
  },
  props: {
    user: Object,
  },
  data: () => ({
    drawer: null,
    alert: false,
    currentModule: null,
    modules: [],
    e6: null,
    filterOptions: ["Show all", "Show only subscribed"],
    items2: [
      {
        title: "Semester oder Modul 1",
        icon: "mdi-view-dashboard",
      },
      {
        title: "Semester oder Modul 2",
        icon: "mdi-forum",
      },
    ],
    items: [
      {
        src: "https://cdn.vuetifyjs.com/images/carousel/squirrel.jpg",
      },
      {
        src: "https://cdn.vuetifyjs.com/images/carousel/sky.jpg",
      },
      {
        src: "https://cdn.vuetifyjs.com/images/carousel/bird.jpg",
      },
      {
        src: "https://cdn.vuetifyjs.com/images/carousel/planet.jpg",
      },
    ],
  }),
  methods: {
    drawNav() {
      this.drawer = !this.drawer;
    },
    subscribeModule(clickedModule) {
      this.currentModule = clickedModule;
      Meteor.call("module.subscribe", clickedModule);
      this.drawer = false;
    },
    openModule(module) {
      this.currentModule = module;
      this.drawer = false;
    },
    filterModules (){
      console.log(this.e6)
    }
  },
  created() {
    Tracker.autorun(() => {
      this.modules = Modules.find().fetch();
      if (this.currentModule === null) {
        this.currentModule = this.modules[0];
      }
    });
  },
};
</script>

